int found[LENGTH];
unsigned char key[LENGTH];

void find_key(void) {
	size_t i, j, col;

	for (col = 0; col < LENGTH; ++col) {
		for (i = 0; i < SAMPLES; ++i) {
			for (j = i + 1; j < SAMPLES; ++j)
				if ((ctab[i][col] ^ ctab[j][col]) & '@') break;
			if (j == SAMPLES) continue;

			found[col] = 1;

			key[col] = ctab[i][col] ^ ' ';
			if (isalpha(key[col] ^ ctab[j][col])) continue;

			key[col] = ctab[j][col] ^ ' ';
			if (isalpha(key[col] ^ ctab[i][col])) continue;

			found[col] = 0;
		}
	}
}

void clean_key(void) {
	size_t col, sample;
	unsigned char c;

	for (col = 0; col < LENGTH; ++col) {
		if (!found[col]) continue;

		for (sample = 0; sample < SAMPLES; ++sample) {
			c = ctab[sample][col] ^ key[col];
			if (!isalpha(c) && c != ' ') {
				found[col] = 0;
				break;
			}
		}
	}
}

void print_text(size_t sample) {
	size_t i;

	for (i = 0; i < LENGTH; ++i) {
		if (!found[i])
			printf("_");
		else
			printf("%c", ctab[sample][i] ^ key[i]);
	}
	printf("\n");
}

int main(void) {
	size_t i;

	find_key();
	clean_key();
	for (i = 0; i < SAMPLES; ++i)
		print_text(i);
	return 0;
}
